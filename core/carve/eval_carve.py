                """Compute first 8 chars of SHA256 checksum."""
                if not file_path.exists():
                    return "FILE_NOT_FOUND"
                with open(file_path, 'rb') as f:
                    sha256_hash = hashlib.sha256(f.read()).hexdigest()
                    return sha256_hash[:8]
            
